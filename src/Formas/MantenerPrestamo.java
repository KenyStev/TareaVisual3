    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String x= JOptionPane.showInputDialog(this, "Cantidad de Cuotas a Pagar", "Cantidad de Cuotas", JOptionPane.QUESTION_MESSAGE);
        int cuotas = Integer.parseInt(x);
        if(prestamo.pagarCuota(cuotas)){
            JOptionPane.showMessageDialog(this,
                    "Se ha pagado Lps. "+prestamo.getMontoPago()+" al prestamo",
                    "Pagado",
                    JOptionPane.PLAIN_MESSAGE);
            txtInfo.setText(prestamo.toString());
            if(prestamo.getSaldo()==0){
                prestamoPagado();
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Cuotas mayores a las restantes!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(this,
                "Realmente desea pagar todo el prestamo?",
                "Confirmacion",
                JOptionPane.OK_CANCEL_OPTION);
        if(confirm == JOptionPane.OK_OPTION){
            JOptionPane.showMessageDialog(this, prestamo.pagarTodo(), "Pagado", JOptionPane.INFORMATION_MESSAGE);
            prestamoPagado();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

